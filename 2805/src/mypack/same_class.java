package mypack;

public class same_class {
	int d;
	private int p;
	protected int pro;
	public int pub;
	public same_class(){
		System.out.println("Default access specifier : "+d);
		System.out.println("private access specifier : "+p);
		System.out.println("protected access specifier : "+pro);
		System.out.println("public access specifier : "+pub);
	}

}
