package gprec.math;

public class Math {
     public int sum(int n,int m)
     {
    	 return n+m;
     }
     public int sub(int n,int m)
     {
    	 return n-m;
     }
     public int mul(int n,int m)
     {
    	 return n*m;
     }
     public int div(int n,int m)
     {
    	 return n/m;
     }
     
}
